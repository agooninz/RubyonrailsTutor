def capital(string)
	string.capitalize
end

def length(string)
	string.length
end

def down(string)
	string.downcase!
end

def tempfix(string)
	string.gsub("*", "")
end

def permfix(string)
	string.gsub!("*", "")
end

def rever(string)
	string.reverse
end

def strp(string)
	string.strip
end

def sym(string)
	string.to_sym
end

def upletter(string)
	string.upcase
end

